// CHANCE_CARDS: each has an icon, a short label (shown while the reel spins),
// an apply(state) that mutates group scores, and a result(state) that returns
// the sentence shown on the reveal screen (after apply() already ran).
function clampScore(n) { return n; } // no floor — scores can go negative
function otherGroupIndex(state, offset) {
  const n = state.groups.length;
  return ((state.currentIndex + offset) % n + n) % n;
}
function lowestGroupIndex(state) {
  let best = 0;
  state.groups.forEach((g, i) => { if (g.score < state.groups[best].score) best = i; });
  return best;
}

